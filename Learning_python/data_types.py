# python data types

# for text --------------> str
print(type("Janisar"))
# for numbers --------------> int, float, complex
print(type(10))
print(type(10.00))
print(type(10j))
# for boolean --------------> bool
print(type(True))
# for sequence data --------------> list , tuple, range
print(type(["janisar","Mallah"]))
print(type(("Janisar","mallah")))
print(type(range(6)))
# for set --------------> set 
print(type({"Janisar","Mallah"}))
# for mapping --------------> dict
print(type({"name":"janisar","age":20}))